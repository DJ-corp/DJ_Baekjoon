# 숫자 처리 함수
print(abs(-5))
print(pow(4,2)) # 제곱
print(max(5,4,15))
print(min(4,2,16))
print(round(215.5415,2)) # 소수점 둘째자리까지 반올림
print(round(215.5415,-1)) # 십의자리까지 반올림
print(int(5.23426)) # 소수점 이하 내림

from math import *

print(floor(3.14)) # 무조건 소수 첫째 자리에서 내림
print(ceil(3.14)) # 무조건 소수 첫째 자리에서 올림
print(sqrt(16)) # 루트
