# 숫자 자료형
print(5)
print(5 + 3)
print(5 % 3) # 나머지
print(5 * (3 + 2))
print(5 // 2) # 몫
print(5 / 2) # 나누기

# 문자 자료형
print("송재준")
print("ㅋ" * 9)
print("a","b") # 한칸 띄고
print("a" + "b") # 안 띄고

# boolean 자료형
print(5 > 10)
print(5 < 10)
print(not(5 < 10))
print(5 == 5)
print(2 != 3)
print(5 > 3 > 2 > 1)
print((5 > 3) and (5 < 3))
print((5 > 3) or (5 < 3))



